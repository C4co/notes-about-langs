#include <iostream>

// main
int main()
{
  // int
  int a = 10;
  std::cout << "int a = " << a << std::endl;

  // float
  float b = 10.5;
  std::cout << "float b = " << b << std::endl;

  // double
  double c = 10.5;
  std::cout << "double c = " << c << std::endl;

  // char
  char d = 'A';
  std::cout << "char d = " << d << std::endl;

  // bool
  bool e = true;
  std::cout << "bool e = " << e << std::endl;

  // string
  std::string f = "Hello World";
  std::cout << "string f = " << f << std::endl;

  // return
  return 0;
}
