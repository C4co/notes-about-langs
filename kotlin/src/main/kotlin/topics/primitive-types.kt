package com.example.topics

fun primitiveTypes() {
    val a: Int = 1
    val b: Long = 1L
    val c: Float = 1.0F
    val d: Double = 1.0
    val e: Boolean = true
    val f: Char = 'a'
    val g: String = "a"

    println("a = $a")
    println("b = $b")
    println("c = $c")
    println("d = $d")
    println("e = $e")
    println("f = $f")
    println("g = $g")
}
