package learn.kotlin.topics.types

val a: Int = 1
val b: Long = 1L
val c: Float = 1.0f
val d: Double = 1.0
val e: Boolean = true
val f: Char = 'a'
val g: String = "a"

fun main() {
    println("a: Int = $a")
    println("b: Long = $b")
    println("c: Float = $c")
    println("d: Double = $d")
    println("e: Boolean = $e")
    println("f: Char = $f")
    println("g: String = $g")
}

