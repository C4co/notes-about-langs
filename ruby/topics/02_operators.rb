# equality
puts 1 == 1
puts 1 == "1"

# inequality
puts 1 != "1"

# falsey values
puts !!nil
puts !!false
puts !!""
puts !!0

# More comparisons
puts 1 < 10 #=> true
puts 1 > 10 #=> false
puts 2 <= 2 #=> true
puts 2 >= 2 #=> true

# Logical operators
puts true && false #=> false
puts true || false #=> true
puts true and false #=> false
puts true or false #=> false
